async function linearGraphQL(key,query,variables){
  const r=await fetch('https://api.linear.app/graphql',{method:'POST',
    headers:{'Content-Type':'application/json','Authorization':key},
    body:JSON.stringify({query:query,variables:variables||{}})});
  const j=await r.json();
  if(j.errors)throw new Error(j.errors.map(e=>e.message).join('; '));
  return j.data;
}

async function createLinearProject(){
  const res=document.getElementById('linearResult');
  const sp=linearProjectSpec();
  if(!sp.name){ res.innerHTML='<div class="banner red">Please enter a project name.</div>'; return; }
  const key=document.getElementById('linKey').value.trim()||DEFAULT_LINEAR_KEY;
  res.innerHTML='<div class="banner">⏳ Creating project in Linear…</div>';
  try{
    /* MEP project label — only set if the workspace exposes project labels */
    let labelIds=[];
    try{
      const ld=await linearGraphQL(key,'{ projectLabels{ nodes{ id name } } }');
      const lab=ld.projectLabels.nodes.find(n=>(n.name||'').toLowerCase()==='mep');
      if(lab)labelIds=[lab.id];
    }catch(e){}
    const input={name:sp.name, teamIds:[LINEAR_IDS.team], description:sp.summary, content:sp.content,
      leadId:LINEAR_IDS.lead, priority:parseInt(sp.priority,10)||0};
    if(sp.targetDate)input.targetDate=sp.targetDate;
    if(labelIds.length)input.labelIds=labelIds;
    const CREATE='mutation($input:ProjectCreateInput!){projectCreate(input:$input){success project{id url}}}';
    let pc;
    try{ pc=(await linearGraphQL(key,CREATE,{input})).projectCreate; }
    catch(e){ /* fall back to the minimal valid project so it always succeeds */
      const min={name:sp.name, teamIds:[LINEAR_IDS.team], description:sp.summary, priority:parseInt(sp.priority,10)||0};
      pc=(await linearGraphQL(key,CREATE,{input:min})).projectCreate;
    }
    if(!pc||!pc.success)throw new Error('Project creation was rejected by Linear.');
    const proj=pc.project;
    /* link initiative */
    const initId=LINEAR_IDS.initiatives[sp.initiative];
    if(initId){ try{ await linearGraphQL(key,'mutation($input:InitiativeToProjectCreateInput!){initiativeToProjectCreate(input:$input){success}}',{input:{initiativeId:initId,projectId:proj.id}}); }catch(e){} }
    /* milestones */
    let mDone=0;
    if(sp.withMilestones){ for(let i=0;i<sp.milestones.length;i++){
      try{ await linearGraphQL(key,'mutation($input:ProjectMilestoneCreateInput!){projectMilestoneCreate(input:$input){success}}',{input:{projectId:proj.id,name:sp.milestones[i],sortOrder:i}}); mDone++; }catch(e){}
    }}
    /* issue per space */
    let iDone=0;
    if(sp.withIssues){
      const list=[]; S.floors.forEach(f=>f.spaces.forEach(s=>list.push({f:f,s:s})));
      for(const x of list){
        try{ await linearGraphQL(key,'mutation($input:IssueCreateInput!){issueCreate(input:$input){success}}',{input:{teamId:LINEAR_IDS.team,projectId:proj.id,title:x.s.name+' ('+x.s.type+')',description:'Floor: '+x.f.name}}); iDone++; }catch(e){}
      }
    }
    res.innerHTML='<div class="banner green">✅ Created <a href="'+proj.url+'" target="_blank" style="text-decoration:underline">'+E(sp.name)+'</a> in Linear (team Ozil)'+(mDone?' · '+mDone+' milestones':'')+(iDone?' · '+iDone+' issues':'')+'.</div>';
  }catch(e){
    const net=/Failed to fetch|NetworkError|Load failed/i.test(e.message||'');
    const tip=net?'Browser blocked the request (CORS/network). Use <strong>⬇ Download .ps1</strong> instead — it needs no http link.'
                 :'Linear rejected the request. Check the API key is valid and try again.';
    res.innerHTML='<div class="banner red">❌ '+E(e.message)+'<br><span style="font-size:11px">'+tip+'</span></div>';
  }
}

/* Offline path: generate a PowerShell script that creates the project via Linear's API,
   run by double-clicking — no browser fetch, so no CORS and no http link required. */
function downloadLinearScript(){
  const res=document.getElementById('linearResult');
  const sp=linearProjectSpec();
  if(!sp.name){ res.innerHTML='<div class="banner red">Please enter a project name.</div>'; return; }
  const issues=[];
  if(sp.withIssues)S.floors.forEach(f=>f.spaces.forEach(s=>issues.push(s.name+' ('+s.type+')')));
  const payload={name:sp.name, teamId:LINEAR_IDS.team, leadId:LINEAR_IDS.lead,
    priority:parseInt(sp.priority,10)||0, targetDate:sp.targetDate||null,
    initiativeId:LINEAR_IDS.initiatives[sp.initiative]||null, summary:sp.summary, content:sp.content,
    milestones:sp.withMilestones?sp.milestones:[], issues:issues};
  const json=JSON.stringify(payload);
  const ps=[
    '# Create-LinearProject.ps1 - generated by MEP Studio.',
    '# Runs fully offline (no browser, no server); needs internet to reach Linear.',
    '$ErrorActionPreference="Stop"',
    '[Net.ServicePointManager]::SecurityProtocol=[Net.SecurityProtocolType]::Tls12',
    "$json=@'",
    json,
    "'@",
    '$p=$json | ConvertFrom-Json',
    '$key="'+DEFAULT_LINEAR_KEY+'"',
    'if(-not $key){ $key=Read-Host "Paste your Linear API Key" }',
    '$headers=@{Authorization=$key;"Content-Type"="application/json"}',
    'function Lin($q,$v){',
    '  $b=@{query=$q;variables=$v} | ConvertTo-Json -Depth 30 -Compress',
    '  $r=Invoke-RestMethod -Uri "https://api.linear.app/graphql" -Method Post -Headers $headers -Body $b',
    '  if($r.errors){ throw (($r.errors | ForEach-Object { $_.message }) -join "; ") }',
    '  return $r.data',
    '}',
    '$labelIds=@()',
    'try{ $ld=Lin "{ projectLabels{ nodes{ id name } } }" @{}; $lab=$ld.projectLabels.nodes | Where-Object { $_.name -eq "MEP" }; if($lab){ $labelIds=@($lab.id) } }catch{}',
    '$pin=@{ name=$p.name; teamIds=@($p.teamId); description=$p.summary; content=$p.content; leadId=$p.leadId; priority=[int]$p.priority }',
    'if($p.targetDate){ $pin.targetDate=$p.targetDate }',
    'if($labelIds.Count -gt 0){ $pin.labelIds=$labelIds }',
    '$CREATE="mutation(`$input:ProjectCreateInput!){projectCreate(input:`$input){success project{id url}}}"',
    '$pc=$null',
    'try{ $pc=(Lin $CREATE @{input=$pin}).projectCreate }',
    'catch{ $min=@{ name=$p.name; teamIds=@($p.teamId); description=$p.summary; priority=[int]$p.priority }; $pc=(Lin $CREATE @{input=$min}).projectCreate }',
    'if(-not $pc.success){ throw "Project creation rejected by Linear." }',
    '$proj=$pc.project',
    'if($p.initiativeId){ try{ Lin "mutation(`$input:InitiativeToProjectCreateInput!){initiativeToProjectCreate(input:`$input){success}}" @{input=@{initiativeId=$p.initiativeId;projectId=$proj.id}} | Out-Null }catch{} }',
    '$i=0',
    'foreach($m in $p.milestones){ try{ Lin "mutation(`$input:ProjectMilestoneCreateInput!){projectMilestoneCreate(input:`$input){success}}" @{input=@{projectId=$proj.id;name=$m;sortOrder=$i}} | Out-Null }catch{}; $i++ }',
    'foreach($t in $p.issues){ try{ Lin "mutation(`$input:IssueCreateInput!){issueCreate(input:`$input){success}}" @{input=@{teamId=$p.teamId;projectId=$proj.id;title=$t}} | Out-Null }catch{} }',
    'Write-Host ""',
    'Write-Host ("Created: " + $proj.url) -ForegroundColor Green',
    'Read-Host "Done. Press Enter to close"'
  ].join("\r\n");
  const blob=new Blob([ps],{type:'application/octet-stream'});
  const a=document.createElement('a'); a.href=URL.createObjectURL(blob);
  a.download='Create-Linear-'+sp.name.replace(/[^a-z0-9]+/gi,'_')+'.ps1'; a.click();
  res.innerHTML='<div class="banner green">⬇️ Script downloaded. Right-click it → <strong>Run with PowerShell</strong> (or run <code>powershell -ExecutionPolicy Bypass -File "the-file.ps1"</code>). Creates the project + milestones'+(sp.withIssues?' + issues':'')+' — no browser, no server, no http link.</div>';
}
